package com.zert.java.tutorial;

public class MathOperation {
	
	final static double pi = 3.14; 
 

	public final static int multiply(int a, int b ) {
	int d = (a*b);
		return d;
		
	}
	
	public final static int sub (int a, int b ) {
		int e = (a-b);
			return e;
					}
	
	public final static double areaOfCircle(double r) {
		double area = pi * r * r;
		return area; 
	}
	
}
